"use client";

import { motion } from "framer-motion";
import { Bookmark, Loader2 } from "lucide-react";
import { useAuth } from "@/context/AuthContext";
import { useState } from "react";

const AuthPage = () => {
    const { signInWithGoogle } = useAuth();
    const [loading, setLoading] = useState(false);

    const handleGoogleSignIn = async () => {
        setLoading(true);
        await signInWithGoogle();
        // loading stays true — page will redirect
    };

    return (
        <div className="min-h-screen flex items-center justify-center relative overflow-hidden bg-background">
            {/* Background blobs */}
            <div className="absolute top-[-10%] left-[-10%] w-[40%] h-[40%] bg-primary/10 bg-blob" />
            <div className="absolute bottom-[-10%] right-[-10%] w-[50%] h-[50%] bg-primary/15 bg-blob" />
            <div className="absolute top-1/2 left-1/2 -translate-x-1/2 -translate-y-1/2 w-64 h-64 bg-primary/10 bg-blob" />

            <motion.div
                initial={{ opacity: 0, y: 20 }}
                animate={{ opacity: 1, y: 0 }}
                transition={{ duration: 0.6, ease: "easeOut" }}
                className="w-[92%] max-w-md"
            >
                <div className="glass rounded-2xl shadow-2xl p-8 sm:p-12 flex flex-col items-center text-center">
                    {/* Icon */}
                    <div className="w-20 h-20 rounded-2xl bg-primary/15 flex items-center justify-center mb-6">
                        <Bookmark className="w-10 h-10 text-primary" />
                    </div>

                    {/* Title */}
                    <h1 className="text-3xl font-bold tracking-tight text-foreground mb-2">
                        Smart Bookmark
                    </h1>
                    <p className="text-muted-foreground text-base mb-8">
                        Your private bookmarks. Always in sync.
                    </p>

                    {/* Google Button */}
                    <motion.button
                        whileHover={{ scale: loading ? 1 : 1.02 }}
                        whileTap={{ scale: loading ? 1 : 0.98 }}
                        onClick={handleGoogleSignIn}
                        disabled={loading}
                        className="w-full py-3.5 px-6 rounded-xl bg-primary text-primary-foreground font-semibold text-base flex items-center justify-center gap-3 hover:glow-primary transition-shadow disabled:opacity-70 disabled:cursor-not-allowed"
                    >
                        {loading ? (
                            <Loader2 className="w-5 h-5 animate-spin" />
                        ) : (
                            <GoogleIcon />
                        )}
                        {loading ? "Signing in…" : "Continue with Google"}
                    </motion.button>

                    {/* Footer */}
                    <p className="mt-8 text-xs text-muted-foreground leading-relaxed">
                        By continuing, you agree to our{" "}
                        <a href="/terms" className="underline hover:text-foreground transition-colors">Terms of Service</a>
                        {" "}and{" "}
                        <a href="/privacy" className="underline hover:text-foreground transition-colors">Privacy Policy</a>.
                    </p>

                    <div className="mt-6 flex items-center gap-3 text-muted-foreground">
                        <div className="h-px flex-1 bg-border" />
                        <span className="text-[10px] font-semibold tracking-[0.15em] uppercase">
                            Secure Cloud Sync
                        </span>
                        <div className="h-px flex-1 bg-border" />
                    </div>
                </div>
            </motion.div>
        </div>
    );
};

const GoogleIcon = () => (
    <svg width="20" height="20" viewBox="0 0 24 24">
        <path
            d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92a5.06 5.06 0 0 1-2.2 3.32v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.1z"
            fill="#4285F4"
        />
        <path
            d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"
            fill="#34A853"
        />
        <path
            d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"
            fill="#FBBC05"
        />
        <path
            d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"
            fill="#EA4335"
        />
    </svg>
);

export default AuthPage;
